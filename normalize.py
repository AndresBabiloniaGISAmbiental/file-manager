def normalize(s):
    replacements = (
        ("á", "a"),
        ("é", "e"),
        ("í", "i"),
        ("ó", "o"),
        ("ú", "u"),
        ("Á", "A"),
        ("É", "E"),
        ("Í", "I"),
        ("Ó", "O"),
        ("Ú", "U"),
        ("\\",""),
        ("?", ""),
        ("¿", ""),
        ("/",""),
        (":",""),
        ("*",""),
        ('"',""),
        ("<",""),
        (">",""),
        ("|",""),
        (" ", "_")
        )
    for a, b in replacements:
        s = s.replace(a, b)
    return s
